                                    <img src="img/custom-bg/custom-bg6.jpg" alt="Marine Food Blog Post">
                                </figure>
                                <div class="recipe-container">
                                    <h3 class="recipe-title">QUICK PICKLE RECIPE</h3>
                                    <ul>
                                        <li>1/2 TEASPOON SALT</li>
                                        <li>1/2 TEASPOON SUGAR</li>
                                        <li>1 CUP MATCHSTICK DAIKONS</li>
                                        <li>1 CUP MATCHSTICK CARROTS</li>
                                    </ul>
                                    <p>In a bowl, sprinkle the salt and sugar over the carrots and daikons. Let stand for 30 minutes, rinse and enjoy. Warm your tortillas, spread with a bit of pate, squirt on a touch of mayo, and pile on the pulled pork. Top with daikon, carrots, jalapenos, cilantro and a squirt of sriracha. Squeeze on some lime and you’re good to go!</p>
